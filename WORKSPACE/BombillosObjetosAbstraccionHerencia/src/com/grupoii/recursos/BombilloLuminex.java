package com.grupoii.recursos;

public class BombilloLuminex {
	
	private String marca;
	private int lumnes;
	
	public BombilloLuminex() {
		this.marca = "Luminex";
		this.lumnes = 20000;
	}

	public String getMarca() {
		return marca;
	}

	public void setMarca(String marca) {
		this.marca = marca;
	}

	public int getLumnes() {
		return lumnes;
	}

	public void setLumnes(int lumnes) {
		this.lumnes = lumnes;
	}

	
}
